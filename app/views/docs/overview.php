<h2>Overview</h2>
<p>PHPasap is a PHP framework for developers who need a MVC framework with quick setup, small footprint and simple MVC architecture. It's for those who love coding as per their style and standard but just need a framework to organise their code with some helpers and libraries for usual tasks like database and sessions.</p>

<p>Simplicity is the key mantra of this framework. Someone even with basic understanding of PHP should be good to use this framework. PHPasap requires almost zero configuration to setup and doesn't require composer or any command line tools for installation (although it supports composer).</p>


